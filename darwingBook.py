def pageCount(n, p):
     depan = p // 2
     belakang = (n-p)//2 + ( 1 if p%2!=0 and n-p == 1 else 0 )
     return belakang if belakang < depan else depan
if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    p = int(input().strip())

    result = pageCount(n, p)

    fptr.write(str(result) + '\n')

    fptr.close()
